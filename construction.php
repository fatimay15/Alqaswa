          <h2>INTRODUCTION</h2>
          <p>Acrylic Emulsion – 3A is a fine particle size, acrylic copolymer emulsion. Its moderate film hardness allows binder plasticity to withstand high pigment loads. It also exhibits superior alkali water and weathering resistance.</p>
