          <h2>FORMULATION GUIDELINES</h2>
          <h3>PIGMENT</h3>
          <p>Acrylic Emulsion – 3A can be formulated to withstand high pigments and extender loads. It associates with clay so much so that one can use a lesser amount of clay to achieve similar performance characteristics.</p>
          <h3>FILM COALESCING AIDS</h3>
          <p>Coalescing aid is required for optimum film formulation under air-dry applications. The amount will depend on the formulation and drying conditions. Texanol at 5-10% by weight on total dry resin is recommended for adequate filming.</p>
          <h3>THICKENERS</h3>
          <p>Acrylic Emulsion – 3A is designed to have a low thickener demand. Although it may be used to increase the viscosity of a coating based on Acrylic Emulsion - FB, it is important to note that the amount of thickeners required is lower compared to when the same thickener is used in conventional acrylic binders.</p>
          <h3>HIGH PVC PAINTS</h3>
